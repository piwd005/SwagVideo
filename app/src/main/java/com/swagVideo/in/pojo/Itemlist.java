package com.swagVideo.in.pojo;

public class Itemlist {
    String img, count;

    public Itemlist(String img, String count) {
        this.img = img;
        this.count = count;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getCount() {
        return count;
    }

    public void setCount(String count) {
        this.count = count;
    }
}
